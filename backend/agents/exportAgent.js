const { jsPDF } = require('jspdf');
const { Document, Packer, Paragraph, TextRun, Table, TableRow, TableCell, WidthType, BorderStyle } = require('docx');
const { Builder } = require('xml2js');
const { sendFormEmail } = require('../utils/email');
const { saveExport } = require('../utils/mongodb');
const { v4: uuidv4 } = require('uuid');

async function exportOccurrenceReport(formData, paramedicProfile) {
  console.log('[ExportAgent] Generating Occurrence Report PDF...');

  const doc = new jsPDF();
  const fields = formData.fields || {};
  const margin = 20;
  const labelX = margin;
  const valueX = 85;
  const maxWidth = 95;
  let y = 20;

  const accentR = 229; const accentG = 62; const accentB = 62; // #e53e3e

  // Form header (matches app: icon area + title + subtitle)
  doc.setFillColor(255, 240, 240); // light red tint
  doc.roundedRect(margin, y - 6, 44, 22, 3, 3, 'F');
  doc.setDrawColor(accentR, accentG, accentB);
  doc.setLineWidth(0.5);
  doc.roundedRect(margin, y - 6, 44, 22, 3, 3, 'S');
  doc.setFontSize(16);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(40, 40, 40);
  doc.text('Occurrence Report', margin + 50, y + 2);
  doc.setFontSize(10);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(100, 100, 100);
  doc.text('Incident Documentation', margin + 50, y + 10);
  y += 28;

  const addSection = (label) => {
    doc.setDrawColor(220, 220, 220);
    doc.setLineWidth(0.3);
    doc.line(margin, y, 190, y);
    y += 4;
    doc.setFontSize(9);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(100, 100, 100);
    doc.text(label.toUpperCase(), margin, y + 3);
    y += 12;
  };

  const addField = (label, value, required = false) => {
    const v = value != null && value !== '' ? String(value) : '—';
    doc.setFontSize(11);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(80, 80, 80);
    doc.text(`${label}${required ? ' *' : ''}`, labelX, y + 4);
    doc.setFillColor(248, 248, 250);
    doc.setDrawColor(230, 230, 235);
    doc.roundedRect(valueX - 2, y - 3, 105, 10, 2, 2, 'FD');
    doc.setTextColor(40, 40, 40);
    doc.text(v, valueX + 101, y + 4, { align: 'right', maxWidth: 99 });
    y += 12;
    if (y > 270) { doc.addPage(); y = 20; }
  };

  addSection('INCIDENT DETAILS');
  addField('Date', fields.date?.value, true);
  addField('Time', fields.time?.value, true);
  addField('Call Number', fields.call_number?.value);
  addField('Occurrence Type', fields.occurrence_type?.value, true);
  addField('Reference #', fields.occurrence_reference?.value);
  addField('Severity', fields.severity?.value, true);

  addSection('VEHICLE & LOCATION');
  addField('Vehicle Number', fields.vehicle_number?.value);
  addField('Vehicle Description', fields.vehicle_description?.value);
  addField('Location', fields.location?.value, true);
  addField('City', fields.city?.value, true);
  addField('Province', fields.province?.value);

  addSection('PARAMEDIC INFO');
  addField('Service', fields.service?.value);
  addField('Role', fields.role?.value, true);
  addField('Badge Number', fields.badge_number?.value, true);
  addField('Paramedic Name', fields.paramedic_name?.value, true);

  addSection('REPORT');
  addField('Injuries Reported', fields.injuries_reported?.value, true);
  addField('Equipment Damage', fields.equipment_damage?.value, true);
  addField('Supervisor Notified', fields.supervisor_notified?.value);
  addField('Other Services', fields.other_services?.value);

  doc.setFont('helvetica', 'normal');
  doc.setTextColor(80, 80, 80);
  doc.text('Description *', labelX, y + 4);
  y += 8;
  doc.setFillColor(248, 248, 250);
  doc.setDrawColor(230, 230, 235);
  const descText = String(fields.description?.value || 'N/A');
  const descLines = doc.splitTextToSize(descText, maxWidth);
  const descH = Math.max(10, descLines.length * 5);
  doc.roundedRect(valueX - 2, y - 3, 105, descH, 2, 2, 'FD');
  doc.setTextColor(40, 40, 40);
  doc.text(descLines, valueX, y + 3);
  y += descH + 10;

  doc.setTextColor(80, 80, 80);
  doc.text('Action Taken *', labelX, y + 4);
  y += 8;
  const actionText = String(fields.action_taken?.value || 'N/A');
  const actionLines = doc.splitTextToSize(actionText, maxWidth);
  const actionH = Math.max(10, actionLines.length * 5);
  doc.roundedRect(valueX - 2, y - 3, 105, actionH, 2, 2, 'FD');
  doc.text(actionLines, valueX, y + 3);
  y += actionH + 10;

  addField('Resolution', fields.resolution?.value);
  addField('Management Notes', fields.management_notes?.value);

  y += 8;
  doc.setDrawColor(220, 220, 220);
  doc.line(margin, y, 190, y);
  y += 10;
  doc.setFontSize(8);
  doc.setTextColor(140, 140, 140);
  doc.text(`Generated by ParaHelper | ${new Date().toISOString()} | Locked Record`, 105, y, { align: 'center' });

  const pdfBuffer = Buffer.from(doc.output('arraybuffer'));
  const fileName = `OccurrenceReport_${paramedicProfile.badge_number}_${Date.now()}.pdf`;

  const emailResult = await sendFormEmail({
    to: 'team01@effectiveai.net',
    subject: `[ParaHelper] Occurrence Report - Unit ${paramedicProfile.unit} - ${fields.date?.value || new Date().toLocaleDateString()}`,
    body: `Occurrence Report submitted by ${paramedicProfile.first_name} ${paramedicProfile.last_name} (${paramedicProfile.badge_number}).\n\nType: ${fields.occurrence_type?.value}\nSeverity: ${fields.severity?.value}\nLocation: ${fields.location?.value}, ${fields.city?.value}\n\nPlease find the PDF attached.`,
    attachments: [{ filename: fileName, content: pdfBuffer }]
  });

  const exportRecord = {
    export_id: `EXP-${uuidv4().substring(0, 8)}`,
    form_id: formData.occurrence_id || `OCC-${uuidv4().substring(0, 8)}`,
    paramedic_id: paramedicProfile.paramedic_id,
    file_type: 'pdf',
    file_name: fileName,
    email_sent_to: 'team01@effectiveai.net',
    email_status: emailResult.success ? 'sent' : 'failed',
    sent_at: new Date(),
    retry_count: 0
  };
  await saveExport(exportRecord);

  console.log(`[ExportAgent] Occurrence Report exported: ${fileName}`);
  return { success: true, fileName, emailResult, pdfBase64: pdfBuffer.toString('base64') };
}

