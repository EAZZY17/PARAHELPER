async function exportStatusReport(formData, paramedicProfile) {
  console.log('[ExportAgent] Generating Status Report Word Document...');

  // Support both form_data.fields and flat statusData
  const fields = formData?.fields || formData || {};
  const getVal = (k) => {
    const v = fields[k];
    return (v && typeof v === 'object' && 'value' in v) ? v.value : v;
  };
  const flatData = {
    acr_completion: getVal('acr_completion'),
    acr_unfinished: getVal('acr_unfinished'),
    driver_license: getVal('driver_license'),
    vaccination: getVal('vaccination'),
    vaccination_issues: getVal('vaccination_issues'),
    education: getVal('education'),
    cme_outstanding: getVal('cme_outstanding'),
    uniform_status: getVal('uniform_status'),
    uniform_credits: getVal('uniform_credits'),
    criminal_record: getVal('criminal_record'),
    acp_cert: getVal('acp_cert'),
    vacation: getVal('vacation'),
    missed_meals: getVal('missed_meals'),
    missed_meals_count: getVal('missed_meals_count'),
    overtime: getVal('overtime'),
    overtime_count: getVal('overtime_count')
  };

  const doc = new Document({
    sections: [{
      properties: {},
      children: [
        new Paragraph({
          children: [
            new TextRun({ text: 'Paramedic Status Report', bold: true, size: 28, font: 'Arial' })
          ],
          spacing: { after: 60 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'Compliance Checklist', size: 22, color: '666666' })
          ],
          spacing: { after: 300 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'STATUS ITEMS', bold: true, size: 18, color: '888888' })
          ],
          spacing: { after: 120 }
        }),
        new Table({
          width: { size: 100, type: WidthType.PERCENTAGE },
          rows: [
            new TableRow({
              children: ['Item', 'Type', 'Description', 'Status', '#', 'Notes'].map(h =>
                new TableCell({
                  children: [new Paragraph({ children: [new TextRun({ text: h, bold: true, size: 18 })] })],
                  shading: { fill: 'E8E8EC' },
                  borders: { top: { style: BorderStyle.SINGLE }, bottom: { style: BorderStyle.SINGLE }, left: { style: BorderStyle.SINGLE }, right: { style: BorderStyle.SINGLE } }
                })
              )
            }),
            ...buildStatusRows(flatData)
          ]
        }),
        new Paragraph({
          children: [
            new TextRun({ text: `Generated by ParaHelper | ${new Date().toISOString()}`, size: 16, italics: true, color: '999999' })
          ],
          spacing: { before: 400 }
        })
      ]
    }]
  });

  const buffer = await Packer.toBuffer(doc);
  const fileName = `StatusReport_${paramedicProfile.badge_number}_${Date.now()}.docx`;

  const emailResult = await sendFormEmail({
    to: paramedicProfile.email || 'team01@effectiveai.net',
    subject: `[ParaHelper] Status Report - ${paramedicProfile.first_name} ${paramedicProfile.last_name} - ${new Date().toLocaleDateString()}`,
    body: `Paramedic Status Report for ${paramedicProfile.first_name} ${paramedicProfile.last_name}.\n\nPlease review the attached Word document.`,
    attachments: [{ filename: fileName, content: buffer }]
  });

  console.log(`[ExportAgent] Status Report exported: ${fileName}`);
  return { success: true, fileName, emailResult, docxBase64: buffer.toString('base64') };
}

function buildStatusRows(data) {
  if (!data) return [];
  const items = [
    { code: 'ACRc', type: 'ACR Completion', desc: 'Unfinished ACRs/PCRs', status: data.acr_completion, issues: data.acr_unfinished, notes: 'Complete within 24 hours' },
    { code: 'CERT-DL', type: 'Drivers License', desc: 'License Validity', status: data.driver_license, issues: 0, notes: 'Driver License Status' },
    { code: 'CERT-Va', type: 'Vaccinations', desc: 'Required vaccinations', status: data.vaccination, issues: data.vaccination_issues, notes: 'Per guidelines' },
    { code: 'CERT-CE', type: 'Education', desc: 'Continuing Education', status: data.education, issues: data.cme_outstanding, notes: 'CME outstanding' },
    { code: 'UNIF', type: 'Uniform', desc: 'Uniform credits', status: data.uniform_status, issues: data.uniform_credits, notes: 'Available credits' },
    { code: 'CRIM', type: 'Criminal Record', desc: 'CRC Check', status: data.criminal_record, issues: 0, notes: 'Criminal Issue Free' },
    { code: 'ACP', type: 'ACP Status', desc: 'ACP Cert Valid', status: data.acp_cert, issues: 0, notes: 'ACP certification' },
    { code: 'VAC', type: 'Vacation', desc: 'Vacation requests', status: data.vacation, issues: 0, notes: 'Yearly vacation' },
    { code: 'MEALS', type: 'Missed Meals', desc: 'Missed Meal Claims', status: data.missed_meals, issues: data.missed_meals_count, notes: 'Claims outstanding' },
    { code: 'OVER', type: 'Overtime', desc: 'Overtime Requests', status: data.overtime, issues: data.overtime_count, notes: 'Claims outstanding' }
  ];

  return items.map(item => new TableRow({
    children: [item.code, item.type, item.desc, item.status || 'N/A', String(item.issues || 0), item.notes].map(val =>
      new TableCell({
        children: [new Paragraph({ children: [new TextRun({ text: String(val), size: 16 })] })],
        borders: { top: { style: BorderStyle.SINGLE }, bottom: { style: BorderStyle.SINGLE }, left: { style: BorderStyle.SINGLE }, right: { style: BorderStyle.SINGLE } }
      })
    )
  }));
}

module.exports = { exportOccurrenceReport, exportTeddyBear, exportStatusReport };
